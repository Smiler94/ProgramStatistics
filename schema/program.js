var mongoose = require('mongoose')

var ProgramSchema = new mongoose.Schema({
    name: String,
    url_iqiyi: String,
    url_tencent: String,
    channel: String,
    is_use: Number,
    meta: {
        createAt: {
            type: Date,
            default: Date.now()
        },
        updateAt: {
            type: Date,
            default: Date.now()
        }
    }
})

ProgramSchema.pre('save', function (next) {
    if (this.isNew) {
        this.meta.createAt = this.meta.updateAt = Date.now()
    } else {
        this.meta.updateAt = Date.now()
    }

    next()
})

ProgramSchema.statics = {
    fetch: function (cb) {
        return this
            .find({})
            .sort('meta.updateAt')
            .exec(cb)
    },
    findById: function (id, cb) {
        return this
            .findOne({_id : id})
            .exec(cb)
    }
}
module.exports = ProgramSchema